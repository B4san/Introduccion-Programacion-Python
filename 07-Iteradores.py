lenguajes = ["Python", "Kotlin", "Java", "JavaScript", "PHP", "Ruby", "GO"]

# Iterador
for lenguaje in lenguajes:
    print(lenguaje)

# Iterador con String
for lenguaje in lenguajes:
    print(f"Estoy aprendiendo {lenguaje}")

# Iterador con numeros
for numero in range(0, 10):
    print(numero)

# Iterador que salta de numero en numero
for numero in range(0, 20, 3):
    print(numero)

